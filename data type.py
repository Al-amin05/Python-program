#integer type
age=22
print(type(age))

#Float type
weight=60.5
print(type(weight))

#Complex type
z=3+4j
print(type(z))

#string type
Firstname="Al-amin"
Secondname="Arif"
print(type(Firstname))
print(type(Secondname))
print(Firstname+Secondname)
print(Firstname.lower())

#Boolean type
isMale=True
isFemale=False
print(type(isMale))
print(isMale == isFemale)